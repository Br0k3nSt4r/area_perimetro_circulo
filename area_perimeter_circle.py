# Programa para calcular el Area y el Perimetro de un Circulo (De radio R)

#---------------------------------------------------------
# LIBRERIAS
#---------------------------------------------------------
import math

print("-------------------------------------------------")
print("Area y Perimetro del Circulo")
print("-------------------------------------------------")

#---------------------------------------------------------
# INPUT
#---------------------------------------------------------

r=int(input("Digite el valor del radio: "))


#---------------------------------------------------------
# PROCESSING
#---------------------------------------------------------

a=math.pi*r**2
p=2*math.pi*r


#---------------------------------------------------------
# OUTPUT
#---------------------------------------------------------

print("-------------------------------------------------")
print("-------------------Resultado---------------------")
print("-------------------------------------------------")
print("El Area del circulo es:"+str(a)+"cm")
print("El Perimetro del circulo es:"+str(p)+"cm")
print("-------------------------------------------------")